"""## 4. поиск и удаление литературной речи"""

def calculate_similarity_ratio(text1: str, text2: str) -> float:
    """считаем похожесть двух текстов через расстояние левенштейна

    возвращает число от 0 (совсем разные) до 1 (одинаковые)
    """
    norm1 = normalize_text(text1)
    norm2 = normalize_text(text2)

    if not norm1 or not norm2:
        return 0.0

    lev_dist = lev_distance(norm1, norm2)
    max_len = max(len(norm1), len(norm2))

    if max_len == 0:
        return 1.0

    return 1.0 - (lev_dist / max_len)

def find_matching_whisper_segments(
    standard_segments: List[ManualSegment],
    whisper_segments: List[WhisperSegment],
    threshold: float = 0.7
) -> List[int]:
    """находим сегменты whisper которые матчатся с литературной речью из ручной разметки

    что делаем:
    - берем сегменты литературной речи из ручной разметки (то что в квадратных скобках)
    - для каждого ищем самый похожий сегмент в whisper через левенштейн
    - если похожесть выше порога - помечаем этот сегмент whisper как литературный

    аргументы:
        standard_segments: список литературных сегментов из ручной разметки
        whisper_segments: все сегменты из whisper
        threshold: порог похожести (0-1) для матчинга, дефолт 0.7

    возвращает:
        список индексов whisper сегментов которые надо пометить как литературные
    """
    standard_indices = []

    for standard_seg in standard_segments:
        if not standard_seg.is_dialect:  # только для литературной речи
            best_match_idx = -1
            best_similarity = 0.0

            # ищем самый похожий сегмент в whisper
            for i, whisper_seg in enumerate(whisper_segments):
                similarity = calculate_similarity_ratio(standard_seg.text, whisper_seg.text)

                if similarity > best_similarity:
                    best_similarity = similarity
                    best_match_idx = i

            # если нашли достаточно похожий и еще не использовали этот индекс
            if best_similarity >= threshold and best_match_idx not in standard_indices:
                standard_indices.append(best_match_idx)
                print(f"нашли матч (похожесть={best_similarity:.2f}):")
                print(f"  ручная разметка: {standard_seg.text[:60]}")
                print(f"  whisper: {whisper_segments[best_match_idx].text[:60]}")

    return standard_indices

