def parse_manual_annotation(text: str) -> List[ManualSegment]:
    """парсим ручную разметку с [литературной речью в скобках] и диалектным текстом

    пример:
    [Как искали раньше потерянные вещи?] А, ну э\\то са\\мое… Тут вот…

    возвращает сегменты которые чередуются между литературной (в скобках) и диалектной речью
    """
    segments = []

    # разбиваем по квадратным скобкам и отслеживаем что внутри и снаружи
    parts = re.split(r'(\[.*?\])', text)

    for part in parts:
        part = part.strip()
        if not part:
            continue

        if part.startswith('[') and part.endswith(']'):
            # литературная речь (интервьюер)
            content = part[1:-1].strip()
            # пропускаем технические комментарии типа [...] или [Соб. ...]
            if content and not content.startswith('…') and not content.startswith('Соб.'):
                segments.append(ManualSegment(text=content, is_dialect=False))
        else:
            # диалектная речь
            if part:
                segments.append(ManualSegment(text=part, is_dialect=True))

    return segments

