"""## 3. нормализация и чистка текстов"""

def normalize_text(text: str, remove_stress_marks: bool = True) -> str:
    """нормализуем текст для сравнения сегментов

    что делаем:
    - убираем знаки ударения (бэкслеши, акценты)
    - переводим в lowercase
    - нормализуем пробелы
    - убираем пунктуацию для лучшего матчинга
    """
    # убираем знаки ударения которые часто встречаются в диалектной транскрипции
    if remove_stress_marks:
        text = re.sub(r'\\', '', text)  # убираем бэкслеши
        text = re.sub(r'[́̀̂̃̄]', '', text)  # убираем акценты

    # в lowercase
    text = text.lower()

    # нормализуем диалектные маркеры
    text = text.replace('_', '')

    # нормализуем пунктуацию и пробелы
    text = re.sub(r'[.,!?;:—–-]+', ' ', text)
    text = re.sub(r'\s+', ' ', text)

    return text.strip()

def clean_text_for_alignment(text: str) -> str:
    """чистим текст для выравнивания (менее агрессивно чем нормализация)

    оставляем часть пунктуации но убираем знаки ударения
    используется только для самого процесса выравнивания через алайнер
    """
    # убираем знаки ударения
    text = re.sub(r'\\', '', text)
    text = re.sub(r'[́̀̂̃̄]', '', text)
    text = text.replace('_', '')

    # нормализуем пробелы
    text = re.sub(r'\s+', ' ', text)

    return text.strip()

