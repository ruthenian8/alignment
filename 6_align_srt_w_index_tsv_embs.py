@dataclass
class WhisperSegment:
    """сегмент из whisper srt - один блок с таймкодами и текстом"""
    start_time: float  # время начала в секундах
    end_time: float    # время конца в секундах
    speaker: str       # спикер (SPEAKER_00, SPEAKER_01 и тд)
    text: str          # сам текст
    is_dialect: bool = True  # True если диалект, False если литературная речь (ставим позже)

@dataclass
class ManualSegment:
    """сегмент из ручной разметки"""
    text: str          # текст сегмента
    is_dialect: bool   # True для диалекта, False для литературной речи в квадратных скобках

def parse_timestamp(timestamp_str: str) -> float:
    """переводим таймкод из srt в секунды

    формат: 00:00:00,031
    """
    time_part = timestamp_str.replace(',', '.')
    parts = time_part.split(':')
    hours = int(parts[0])
    minutes = int(parts[1])
    seconds = float(parts[2])
    return hours * 3600 + minutes * 60 + seconds

def parse_whisper_srt(srt_text: str) -> List[WhisperSegment]:
    """парсим whisper srt формат в сегменты

    пример блока в srt:
    1
    00:00:00,031 --> 00:00:05,739
    [SPEAKER_01]: Текст здесь
    """
    segments = []

    # разбиваем на блоки по двойным переносам строк
    blocks = re.split(r'\n\n+', srt_text.strip())

    for block in blocks:
        lines = block.strip().split('\n')
        if len(lines) < 3:
            continue

        # парсим строку с таймкодами
        timestamp_match = re.search(r'(\d{2}:\d{2}:\d{2},\d{3})\s*-->\s*(\d{2}:\d{2}:\d{2},\d{3})', lines[1])
        if not timestamp_match:
            continue

        start_time = parse_timestamp(timestamp_match.group(1))
        end_time = parse_timestamp(timestamp_match.group(2))

        # парсим спикера и текст
        text_line = lines[2]
        speaker_match = re.match(r'\[SPEAKER_(\d+)\]:\s*(.+)', text_line)

        if speaker_match:
            speaker = f"SPEAKER_{speaker_match.group(1)}"
            text = speaker_match.group(2).strip()
        else:
            speaker = "UNKNOWN"
            text = text_line.strip()

        segments.append(WhisperSegment(
            start_time=start_time,
            end_time=end_time,
            speaker=speaker,
            text=text
        ))

    return segments

