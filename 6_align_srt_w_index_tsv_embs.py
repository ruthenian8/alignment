"""## 5. кастомная сегментация для lingtrain aligner"""

def segment_text_with_pauses(text: str, pause_marker: str = '…') -> List[str]:
    """сегментируем текст с учетом маркеров пауз (…) которые показывают естественные разрывы

    это важно потому что:
    - в ручной разметке используют … для пауз
    - в whisper могут быть полные предложения без пауз
    - нам надо чтобы сегментация была более-менее похожей

    стратегия:
    1. сначала разбиваем по маркерам пауз (…) и знакам препинания
    2. потом разбиваем каждую часть по границам предложений если слишком длинная
    3. склеиваем очень короткие сегменты
    """
    # сначала разбиваем по маркерам пауз и сильной пунктуации
    parts = re.split(r'[.!?]+', text)

    segments = []
    current_segment = ""

    for part in parts:
        part = part.strip()
        if not part:
            continue

        # если текущий сегмент пустой - начинаем новый
        if not current_segment:
            current_segment = part
        # если добавление этой части сделает сегмент слишком длинным - сохраняем текущий и начинаем новый
        elif len(current_segment.split()) + len(part.split()) > 30:
            segments.append(current_segment)
            current_segment = part
        # если текущий сегмент очень короткий - склеиваем
        elif len(current_segment.split()) < 5:
            current_segment += " " + part
        # иначе сохраняем текущий и начинаем новый
        else:
            segments.append(current_segment)
            current_segment = part

    # добавляем последний сегмент
    if current_segment:
        segments.append(current_segment)

    return [s.strip() for s in segments if s.strip()]

def prepare_text_for_alignment(segments: List[str], join_short: bool = True) -> List[Tuple[str, str]]:
    """готовим сегментированный текст для выравнивания

    ВАЖНО: теперь возвращаем пары (оригинал, почищенный_для_алайнера)
    это нужно чтобы потом смапить выравненные пары обратно на оригинальные тексты со слэшами

    что делаем:
    - сохраняем оригинальный сегмент как есть
    - делаем почищенную версию (убираем знаки ударения и тд) для самого выравнивания
    - опционально склеиваем очень короткие сегменты (меньше 3 слов) со следующим

    аргументы:
        segments: список текстовых сегментов (оригинальных)
        join_short: склеивать ли очень короткие сегменты, дефолт True

    возвращает:
        список пар (оригинальный_текст, почищенный_текст)
    """
    # создаем пары оригинал-почищенный
    pairs = [(seg, clean_text_for_alignment(seg)) for seg in segments]

    # фильтруем пустые
    pairs = [(orig, clean) for orig, clean in pairs if clean]

    if not join_short:
        return pairs

    # склеиваем очень короткие сегменты (< 3 слов) со следующим
    result = []
    i = 0
    while i < len(pairs):
        orig_current, clean_current = pairs[i]

        # если этот сегмент короткий и есть следующий - склеиваем
        if len(clean_current.split()) < 3 and i + 1 < len(pairs):
            orig_next, clean_next = pairs[i + 1]
            orig_current = orig_current + " " + orig_next
            clean_current = clean_current + " " + clean_next
            i += 2
        else:
            i += 1

        result.append((orig_current, clean_current))

    return result

